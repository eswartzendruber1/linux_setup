print("Zero to Nine:")
for x in range(10):
    print(x)

print("\nFive to 11:")
for x in range(5,12):
    print(x)

print("\n10 to 40 in steps of 5:")
for x in range(10,40,5):
    print(x)

print("\nWhile loop:")
buttcrack = 5
while buttcrack < 10:
    print(buttcrack)
    buttcrack += 1 # Adds 1 every time
